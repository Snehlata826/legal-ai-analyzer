def train_val_test_split(
    texts: List[str],
    labels: List[str],
    val_ratio: float = 0.10,
    test_ratio: float = 0.15,
    seed: int = 42,
) -> Tuple:
    random.seed(seed)
    buckets: dict = {}
    for t, l in zip(texts, labels):
        buckets.setdefault(l, []).append((t, l))

    train, val, test = [], [], []
    for group in buckets.values():
        random.shuffle(group)
        n = len(group)
        n_test = max(3, int(n * test_ratio))
        n_val  = max(3, int(n * val_ratio))
        test  += group[:n_test]
        val   += group[n_test:n_test + n_val]
        train += group[n_test + n_val:]

    random.shuffle(train)
    random.shuffle(val)
    random.shuffle(test)

    return (
        [x for x, _ in train], [x for x, _ in val], [x for x, _ in test],
        [y for _, y in train], [y for _, y in val], [y for _, y in test],
    )


